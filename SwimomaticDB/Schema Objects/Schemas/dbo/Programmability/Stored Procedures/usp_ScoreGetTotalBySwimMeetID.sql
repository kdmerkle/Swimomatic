 @SwimMeetID int

as

	declare @Score table(TeamID int
						,Abbrev varchar(5)
						,Points decimal(14,4))

	--Get Individual Event Scores
	insert into @Score (TeamID,Abbrev,Points)
	select
		 TeamID
		,Abbrev
		,sum(Points) as Points
	from dbo.vwScore
	where SwimMeetID = @SwimMeetID
	and IsRelay = 0
	group by TeamID
			,Abbrev

	--Get Relay Event Scores
	insert into @Score (TeamID,Abbrev,Points)
	select
		 TeamID
		,Abbrev
		,sum(Points/4) as Points
	from dbo.vwScore
	where SwimMeetID = @SwimMeetID
	and IsRelay = 1
	group by TeamID
			,Abbrev

	select
		 TeamID
		,Abbrev
		,sum(Points) as Points
	from @Score
	group by TeamID
			,Abbrev
	order by Abbrev
go

