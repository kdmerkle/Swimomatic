 The Entity expects the following properties to be returned:
   SwimMeetID < System.Int32 >
   Disqualified < System.Boolean >
   TeamID < System.Int32 >
   ElapsedTime < System.Double >
   Swimmer < System.String >
   LaneLength < System.Double >
