	from dbo.UserLocation ul
	inner join dbo.Location l on l.LocationID = ul.LocationID
	inner join dbo.Region r on r.RegionID = l.RegionID
	inner join dbo.[Pool] p on p.LocationID = l.LocationID
	where ul.SystemUserID = @SystemUserID
	group by ul.SystemUserID
			,ul.UserLocationID
			,l.[Address]
			,l.City
