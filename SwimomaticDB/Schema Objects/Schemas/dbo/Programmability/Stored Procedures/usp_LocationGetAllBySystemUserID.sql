 @SystemUserID int

as
	select
		 ul.SystemUserID
		,ul.UserLocationID
		,l.[Address]
		,l.City
		,l.CreatedByUserID
		,l.CreatedDate
		,l.Latitude
		,l.LocationID
		,l.Longitude
		,l.ModifiedByUserID
		,l.ModifiedDate
		,l.Name
		,l.PostalCode
		,l.RegionID
		,r.RegionAbbrev
